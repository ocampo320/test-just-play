extension HexColor on Color {
  static Color fromHex(String hexCode) {
    hexCode = hexCode.replaceAll('#', '');

    if (hexCode.length == 6) {
      hexCode = 'FF$hexCode';
    }
    return Color(int.parse(hexCode, radix: 16)); // Opacity 100%
  }
}
